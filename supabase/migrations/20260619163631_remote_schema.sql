CREATE OR REPLACE FUNCTION private.is_senior_on_visible_request(senior_id integer)
 RETURNS boolean
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_my_user_id integer;
  v_my_role user_role;
BEGIN
  -- Obter o ID do utilizador autenticado usando a função nativa da sua BD
  v_my_user_id := private.get_my_user_id();
  
  -- Se não estiver autenticado, não permitir
  IF v_my_user_id IS NULL THEN
    RETURN false;
  END IF;

  -- Obter a role do utilizador
  SELECT role INTO v_my_role FROM public.users WHERE id = v_my_user_id;

  -- Se for o próprio sénior a consultar os seus dados, permitir
  IF v_my_user_id = senior_id THEN
    RETURN true;
  END IF;

  -- Se for um voluntário, permitir ver se o sénior tem um pedido público pendente
  -- ou se o voluntário é o responsável pelo pedido
  IF v_my_role = 'VOLUNTEER'::user_role THEN
    RETURN EXISTS (
      SELECT 1 FROM public.requests r
      WHERE r.id_senior = senior_id
      AND (
        (r.state = 'PENDING'::req_state AND r.is_public = true)
        OR (r.state = 'ACCEPTED'::req_state AND r.id_volunteer = v_my_user_id)
      )
    );
  END IF;

  -- Se for um cuidador (Caretaker), permitir ver os seus séniores associados
  IF v_my_role = 'CARETAKER'::user_role THEN
    RETURN EXISTS (
      SELECT 1 FROM public.senior_caretaker sc
      WHERE sc.id_senior = senior_id
      AND sc.id_caretaker = v_my_user_id
    );
  END IF;

  RETURN false;
END;
$function$
;

