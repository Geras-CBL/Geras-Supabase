CREATE OR REPLACE FUNCTION public.on_request_change_create_notification()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  senior_name TEXT;
  caretaker_id INT;
BEGIN
  -- Obter o nome do sénior dinamicamente
  SELECT name INTO senior_name FROM public.users WHERE id = NEW.id_senior;

  -- Obter o ID do cuidador associado ao sénior (se existir)
  SELECT id_caretaker INTO caretaker_id FROM public.senior_caretaker WHERE id_senior = NEW.id_senior LIMIT 1;

  -- ==========================================
  -- FLUXO DE INSERÇÃO (NOVO PEDIDO CRIADO)
  -- ==========================================
  IF TG_OP = 'INSERT' THEN
    IF NEW.is_public THEN
      -- A. Notificar o cuidador sobre o pedido feito à comunidade (community_request)
      IF caretaker_id IS NOT NULL THEN
        INSERT INTO public.notifications (type, description, id_senior, id_caretaker, id_request)
        VALUES (
          'community_request',
          senior_name || ' colocou um pedido de ' || COALESCE(NEW.category, 'ajuda') || ' na comunidade de voluntários.',
          NEW.id_senior,
          caretaker_id,
          NEW.id
        );
      END IF;

      -- B. Notificar a comunidade de voluntários (request público, mantendo o id_senior para a distância geográfica)
      INSERT INTO public.notifications (type, description, id_senior, id_request)
      VALUES (
        'request',
        'Novo pedido de ajuda disponível: ' || COALESCE(NEW.category, 'Pedido'),
        NEW.id_senior,
        NEW.id
      );
    ELSE
      -- Pedido direto ao cuidador:
      -- Tipo 'alert' para que envie notificação push privada ao cuidador e seja filtrado no sénior
      IF NEW.id_caretaker IS NOT NULL THEN
        INSERT INTO public.notifications (type, description, id_senior, id_caretaker, id_request)
        VALUES (
          'alert',
          senior_name || ' fez um novo pedido: ' || COALESCE(NEW.category, 'Pedido'),
          NEW.id_senior,
          NEW.id_caretaker,
          NEW.id
        );
      END IF;
    END IF;

  -- ==========================================
  -- FLUXO DE ATUALIZAÇÃO (MUDANÇA DE ESTADO)
  -- ==========================================
  ELSIF TG_OP = 'UPDATE' THEN
    IF NEW.state = 'ACCEPTED' AND OLD.state = 'PENDING' THEN
      INSERT INTO public.notifications (type, description, id_senior, id_volunteer, id_request)
      VALUES (
        'accepted_request',
        'O seu pedido foi aceite por um voluntário.',
        NEW.id_senior,
        NEW.id_volunteer,
        NEW.id
      );
    END IF;

    IF NEW.state = 'COMPLETED' AND OLD.state != 'COMPLETED' THEN
      INSERT INTO public.notifications (type, description, id_senior, id_volunteer, id_request)
      VALUES (
        'completed_request',
        'O seu pedido de ' || COALESCE(NEW.category, 'ajuda') || ' foi concluído. Clique para avaliar.',
        NEW.id_senior,
        NEW.id_volunteer,
        NEW.id
      );
    END IF;

    IF NEW.state = 'CANCELLED' AND OLD.state != 'CANCELLED' THEN
      INSERT INTO public.notifications (type, description, id_senior, id_volunteer, id_request)
      VALUES (
        'cancelled_request',
        'O seu pedido de ' || COALESCE(NEW.category, 'ajuda') || ' foi cancelado.',
        NEW.id_senior,
        NEW.id_volunteer,
        NEW.id
      );
    END IF;
  END IF;

  RETURN NEW;
END;
$function$
;

