CREATE OR REPLACE FUNCTION public.increment_voucher_tasks_on_request_complete()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
  -- Só atuar quando o estado muda para COMPLETED
  IF NEW.state = 'COMPLETED' AND (OLD.state IS DISTINCT FROM 'COMPLETED') THEN
    -- Verificar se há um voluntário associado ao pedido
    IF NEW.id_volunteer IS NOT NULL THEN
      -- Incrementar current_tasks em todos os vouchers do voluntário que não estejam USED
      UPDATE public.vouchers_volunteer
      SET current_tasks = current_tasks + 1
      WHERE id_volunteer = NEW.id_volunteer
        AND status != 'USED';
    END IF;
  END IF;

  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.notify_caretaker_on_abnormal_sensor()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  sensor_record RECORD;
  is_abnormal BOOLEAN := FALSE;
  current_hour INTEGER;
BEGIN
  -- Extrair a hora atual
  current_hour := EXTRACT(HOUR FROM CURRENT_TIMESTAMP);

  -- Regras de anomalia (exemplo)
  IF NEW.type = 'motion' AND (current_hour >= 0 AND current_hour <= 5) THEN
    -- Movimento detetado de madrugada (entre meia-noite e 5h)
    is_abnormal := TRUE;
  ELSIF NEW.value ILIKE '%alarm%' OR NEW.value ILIKE '%danger%' THEN
    -- String explicita de alarme
    is_abnormal := TRUE;
  END IF;

  IF is_abnormal THEN
    -- Obter os dados do sensor para saber quem avisar
    SELECT id_caretaker, id_senior INTO sensor_record 
    FROM public.sensors 
    WHERE id = NEW.id_sensor;

    IF FOUND AND sensor_record.id_caretaker IS NOT NULL THEN
      INSERT INTO public.notifications (type, description, id_caretaker, id_senior)
      VALUES (
        'alert',
        'Alerta de Sensor: Atividade invulgar detetada (' || NEW.type || ')',
        sensor_record.id_caretaker,
        sensor_record.id_senior
      );
    END IF;
  END IF;

  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.notify_senior_on_request_accepted()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  accepter_name text;
BEGIN
  -- Se o estado mudou para ACCEPTED
  IF NEW.state = 'ACCEPTED' AND OLD.state != 'ACCEPTED' THEN
    
    -- Tentar obter o nome de quem aceitou (Voluntário ou Cuidador)
    -- Num pedido público, quem aceita é o id_volunteer
    IF NEW.id_volunteer IS NOT NULL THEN
      SELECT name INTO accepter_name FROM public.users WHERE id = NEW.id_volunteer;
    -- Se não for público, quem aceita é o id_caretaker
    ELSIF NEW.id_caretaker IS NOT NULL THEN
      SELECT name INTO accepter_name FROM public.users WHERE id = NEW.id_caretaker;
    END IF;

    -- Inserir notificação destinada ao Sénior
    INSERT INTO public.notifications (type, description, id_senior)
    VALUES (
      'accepted_request',
      'O teu pedido de ' || COALESCE(NEW.category, 'ajuda') || ' foi aceite por ' || COALESCE(accepter_name, 'um voluntário') || '!',
      NEW.id_senior
    );
  END IF;

  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.rls_auto_enable()
 RETURNS event_trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'pg_catalog'
AS $function$
DECLARE
  cmd record;
BEGIN
  FOR cmd IN
    SELECT *
    FROM pg_event_trigger_ddl_commands()
    WHERE command_tag IN ('CREATE TABLE', 'CREATE TABLE AS', 'SELECT INTO')
      AND object_type IN ('table','partitioned table')
  LOOP
     IF cmd.schema_name IS NOT NULL AND cmd.schema_name IN ('public') AND cmd.schema_name NOT IN ('pg_catalog','information_schema') AND cmd.schema_name NOT LIKE 'pg_toast%' AND cmd.schema_name NOT LIKE 'pg_temp%' THEN
      BEGIN
        EXECUTE format('alter table if exists %s enable row level security', cmd.object_identity);
        RAISE LOG 'rls_auto_enable: enabled RLS on %', cmd.object_identity;
      EXCEPTION
        WHEN OTHERS THEN
          RAISE LOG 'rls_auto_enable: failed to enable RLS on %', cmd.object_identity;
      END;
     ELSE
        RAISE LOG 'rls_auto_enable: skip % (either system schema or not in enforced list: %.)', cmd.object_identity, cmd.schema_name;
     END IF;
  END LOOP;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.send_expo_push_notification()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  user_push_token text;
  payload jsonb;
BEGIN
  -- Tentar obter token do cuidador (alertas e pedidos)
  IF NEW.id_caretaker IS NOT NULL THEN
    SELECT push_token INTO user_push_token FROM public.users WHERE id = NEW.id_caretaker;
  END IF;

  -- Tentar obter token do sénior (ex: lembretes de medicação)
  IF user_push_token IS NULL AND NEW.id_senior IS NOT NULL THEN
    SELECT push_token INTO user_push_token FROM public.users WHERE id = NEW.id_senior;
  END IF;

  -- Tentar obter token do voluntário
  IF user_push_token IS NULL AND NEW.id_volunteer IS NOT NULL THEN
    SELECT push_token INTO user_push_token FROM public.users WHERE id = NEW.id_volunteer;
  END IF;

  IF user_push_token IS NOT NULL THEN
    payload := json_build_object(
      'to',    user_push_token,
      'sound', 'default',
      'title', CASE 
                 WHEN NEW.type = 'medication' THEN '💊 Lembrete de Medicação'
                 WHEN NEW.type = 'alert' THEN '🚨 Alerta de Segurança'
                 WHEN NEW.type = 'request' THEN '🔔 Novo Pedido'
                 ELSE '🔔 Geras'
               END,
      'body',  NEW.description
    )::jsonb;

    PERFORM net.http_post(
      url     := 'https://exp.host/--/api/v2/push/send'::text,
      body    := payload,
      headers := '{"Content-Type": "application/json"}'::jsonb
    );
  END IF;

  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.send_push_to_all_volunteers()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  vol RECORD;
  payload text;
BEGIN
  -- Iterar sobre todos os voluntários com push_token registado
  FOR vol IN
    SELECT id, push_token
    FROM public.users
    WHERE role = 'VOLUNTEER'
      AND push_token IS NOT NULL
      AND push_token <> ''
  LOOP
    payload := json_build_object(
      'to',    vol.push_token,
      'sound', 'default',
      'title', '🤝 Novo Pedido de Ajuda',
      'body',  COALESCE(NEW.description, 'Há um pedido de ajuda na tua área!')
    )::text;

    PERFORM net.http_post(
      url      := 'https://exp.host/--/api/v2/push/send'::text,
      body     := payload::jsonb,
      headers  := '{"Content-Type": "application/json", "Accept": "application/json"}'::jsonb
    );
  END LOOP;

  RETURN NEW;
END;
$function$
;

